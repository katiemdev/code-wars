//You are given a string of letters and an array of numbers. The numbers indicate positions of letters that must be removed, in order, starting from the beginning of the array. After each removal the size of the string decreases (there is no empty space). Return the only letter left.
function lastSurvivor(letters, coords) {
	while (coords.length != 0) {
		letters = letters.slice(0, coords[0]) + letters.slice(coords[0] + 1);
		coords.shift();
	}
	return letters;
}

//You have cultivated a plant, and after months of hard work, the time has come to reap the flowers of your hard work. When it was growing, you added water and fertilizer, and kept a constant temperature. It's time check how much your plant has grown. A plant is represented horizontally, from the base to the left, to the end to the right. The stem is represented by hyphens -, and the flowers are represented by symbols. A plant always starts with the stem, and always ends with flowers. Four parameters will be given. The four parameters are:
// seed (string) - determines the type of flowers generated by the plant.
// water (integer) - each unit of water extends the portion of stem between the flowers. It also gives how many times the stems + flower clusters should be repeated
// fert (integer) - each unit of fertilizer increases the amount of flowers, grouped in clusters
// temp (integer) - if the temperature given is in the range of 20°C and 30°C, the plant grows normally, otherwise, all the flowers die except for one flower at the end of the stem.
// Given the above parameters, your task is to return a string representing the plant.
function plant(seed, water, fert, temp) {
	if (temp < 20 || temp > 30) {
		return `${"-".repeat(water).repeat(water)}${seed}`;
	} else {
		return `${("-".repeat(water) + seed.repeat(fert)).repeat(water)}`;
	}
}

